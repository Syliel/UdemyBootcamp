//The Switch statement is another control-flow statement that can replace multiple statements.
//I find the syntax unweidly and hard to remember, but it's good to know about!

const day = 2;
switch (day) {
    case 1:
        console.log("Monday!");
        break;
    case 2:
        console.log("Tuesday!");
        break;
    case 3:
        console.log("Wednesday!");
        break;
    case 4:
        console.log("Thursday!");
        break;
    case 5:
        console.log("Friday!");
        break;
    default:
        console.log("Invalid Number!")
}